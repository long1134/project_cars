package info;

public class Store {
    public String nameCar;
    public String cost;
    public String quality;
    public String dayCome;
    public String statusCar;

    public Store(String nameCar, String cost, String quality, String dayCome, String statusCar)
    {
        this.cost = cost;
        this.dayCome = dayCome;
        this.nameCar = nameCar;
        this.quality = quality;
        this.statusCar = statusCar;
    }
}
