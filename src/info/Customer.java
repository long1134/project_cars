package info;

public class Customer {

    public String id;
    public String name;
    public String age;
    public String type;
    public String phone;

    public Customer(){}

    public Customer(String id,String name,String age,String type,String phone){
        this.id=id;
        this.name=name;
        this.age=age;
        this.type=type;
        this.phone=phone;
    }

}
