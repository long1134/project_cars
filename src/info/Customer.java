package info;

public class Customer {

    public String id;
    public String name;
    public String age;
    public String type;
    public String phone;
    public String email;
    public String address;

    public Customer(String id,String name,String age,String type,String phone, String email, String address){
        this.id=id;
        this.name=name;
        this.age=age;
        this.type=type;
        this.phone=phone;
        this.email = email;
        this.address = address;
    }

}
